import 'package:path/path.dart' as p;

String? extractSubdirectoryFromRelativePath(String path, String folderPath) {
  var otherDir = path
      .replaceAll(folderPath, '')
      .split(p.separator)
      // remove
      .skipWhile((value) => value.isEmpty);
  // the last element is the filename
  if (otherDir.length > 1) {
    return p.joinAll(otherDir.take(otherDir.length - 1));
  } else {
    // We are not in a subdirectory
    return null;
  }
}
